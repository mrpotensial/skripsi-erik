     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function download($nib)
    {
        $guestland = \App\Models\GuestLand::firstWhere('nib', '=', $nib);
        $url = public_path() . ('/storage/') . $guestland->peta_bidang;
        // dd($url);
        // return \Illuminate\Support\Facades\Storage::download($url);
        $headers = array(
            'Content-Type: application/pdf',
        );

        return response()->download($url, $guestland->nama_pemilik . "_" . $guestland->nomor_sertifikat . "_" . $guestland->nib . '.pdf', $headers);
    }

